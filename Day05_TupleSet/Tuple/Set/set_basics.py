# Set Basics

# Creating a set
fruits = {"apple", "banana", "mango"}
print("Fruits Set:", fruits)

# Adding element
fruits.add("orange")
print("After add:", fruits)

# Removing element
fruits.remove("banana")
print("After remove:", fruits)

# Uniqueness
numbers = {1, 2, 2, 3, 4}
print("Numbers Set:", numbers)  # Duplicate 2 removed
